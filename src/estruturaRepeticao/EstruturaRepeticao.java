package estruturaRepeticao;
import java.util.Scanner;
public class EstruturaRepeticao {

    public static void main(String[] args){
        int rep = 8;
        System.out.println("**********");
        while(rep >= 0){
            System.out.println("**      **");
            rep--;
        }
        System.out.println("**********");
    }
}
